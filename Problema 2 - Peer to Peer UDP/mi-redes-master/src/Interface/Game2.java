    private void clickActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clickActionPerformed
        if(!roleta && p.isMyTurn()){
            try {
                r = p.roullete();                    
                if (r != Game.SKIP_PLAYER && r != Game.RESET_VALUE) {
                    aux.setValor(r);
                    roleta = true;
                }else if(r == Game.RESET_VALUE){
                    JOptionPane.showMessageDialog(null, "Perdeu Tudo!");
                }else if(r == Game.SKIP_PLAYER){
                    JOptionPane.showMessageDialog(null, "Passou a Vez!");
                }
                    
            } catch (IOException ex) {
                Logger.getLogger(Game2.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
            
        else{
            JOptionPane.showMessageDialog(null, "Não é sua vez");
        }
    }//GEN-LAST:event_clickActionPerformed


