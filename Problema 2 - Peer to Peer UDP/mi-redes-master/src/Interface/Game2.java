    public void pegarLetra(JButton button) {
        if(roleta){
        
        button.setEnabled(false);
        atual = button.getText();

        try {
            game();
            roleta = false;
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(Game2.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
        else if(r != Game.SKIP_PLAYER || r != Game.RESET_VALUE){
           JOptionPane.showMessageDialog(null, "Rode a roleta");
        }
    }

    public void game() throws IOException, ClassNotFoundException {

        if (!p.isGameFinished()) {
            if (p.isMyTurn() && !p.isRoundFinished()) {
                    char c = atual.toUpperCase().charAt(0);
                    int number = p.tryCharacter(c);
                    getWord().setText(p.getWord());
                    //System.out.println("\nAcertou " + number + " letras");
            }
            else if(r == Game.RESET_VALUE){
                    JOptionPane.showMessageDialog(null, "Perdeu Tudo!");
                } 
                
            else {
                    JOptionPane.showMessageDialog(null, "Passou a vez!");
                }

            if (p.isRoundFinished()) {
                JOptionPane.showMessageDialog(null, "Palavra era: " + p.getWord());
                p.nextRound();
                carregarBotao();
            }

            if (p.isGameFinished()) {
                p.sendResult();
                //p.getRankingInfo();
                setVisible(false);
                try{
                    
                    if( Integer.parseInt(pv1.getText()) < Integer.parseInt(pv2.getText())){
                        JOptionPane.showMessageDialog(null, "Você Venceu!");
                    }else{
                        JOptionPane.showMessageDialog(null, "Você Perdeu!");
                    }
                   
                p.sendResult();
                new Ranking(client);
                
                }catch(Exception e){
                    new Home();
                }
            }
        }
    }

